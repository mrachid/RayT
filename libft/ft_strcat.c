#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		i;
	int		j;
	char	*temp;

	i = 0;
	j = 0;
	temp = s1;
	while (temp[i] != '\0')
	{
		s1[i] = temp[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		s1[i++] = s2[j++];
	}
	s1[i] = '\0';
	return (s1);
}
